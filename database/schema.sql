-- Удаление старых таблиц (если есть)
DROP TABLE IF EXISTS order_items CASCADE;
DROP TABLE IF EXISTS orders CASCADE;
DROP TABLE IF EXISTS products CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS pickup_points CASCADE;

-- =====================================================
-- 1. СОЗДАНИЕ ТАБЛИЦ
-- =====================================================

-- Таблица пользователей
CREATE TABLE users (
    id SERIAL PRIMARY KEY,
    full_name VARCHAR(255) NOT NULL,
    login VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    role VARCHAR(50) NOT NULL CHECK (role IN ('Администратор', 'Менеджер', 'Авторизированный клиент'))
);

-- Таблица товаров
CREATE TABLE products (
    id SERIAL PRIMARY KEY,
    article VARCHAR(20) UNIQUE NOT NULL,
    name VARCHAR(255) NOT NULL,
    unit VARCHAR(10) DEFAULT 'шт.',
    price DECIMAL(10, 2) NOT NULL,
    supplier VARCHAR(100),
    manufacturer VARCHAR(100),
    category VARCHAR(100),
    discount INTEGER DEFAULT 0 CHECK (discount >= 0 AND discount <= 100),
    stock INTEGER DEFAULT 0,
    description TEXT,
    photo VARCHAR(255)
);

-- Таблица пунктов выдачи
CREATE TABLE pickup_points (
    id SERIAL PRIMARY KEY,
    address TEXT UNIQUE NOT NULL
);

-- Таблица заказов
CREATE TABLE orders (
    id SERIAL PRIMARY KEY,
    order_number INTEGER UNIQUE NOT NULL,
    user_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
    order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    delivery_date DATE,
    pickup_point_id INTEGER REFERENCES pickup_points(id) ON DELETE SET NULL,
    status VARCHAR(50) DEFAULT 'Новый' CHECK (status IN ('Новый', 'Завершен', 'Отменен')),
    pickup_code VARCHAR(10)
);

-- Таблица состава заказа
CREATE TABLE order_items (
    id SERIAL PRIMARY KEY,
    order_id INTEGER REFERENCES orders(id) ON DELETE CASCADE,
    product_id INTEGER REFERENCES products(id) ON DELETE CASCADE,
    quantity INTEGER NOT NULL CHECK (quantity > 0)
);

